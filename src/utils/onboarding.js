export function formatOnboardingMessage(template, userData = {}) {
  let message = template;
  
  // Replace placeholders
  message = message.replace(/\{\{username\}\}/g, userData.username || '–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ');
  message = message.replace(/\{\{telegramId\}\}/g, userData.telegramId || '–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ');
  message = message.replace(/\{\{workspace\}\}/g, userData.workspace || '–ù–µ –≤—ã–±—Ä–∞–Ω–æ');
  
  return message;
}
